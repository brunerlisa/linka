/** Mini line chart for phone screens and cards - accepts points array 0-1 */
export function MiniChart({ className, points = [0.3, 0.5, 0.4, 0.7, 0.6, 0.8], up = true }) {
  const w = 100
  const h = 30
  const pad = 2
  const p = points.length <= 1 ? [points[0] ?? 0.5, points[0] ?? 0.5] : points
  const pts = p.map((y, i) => {
    const x = pad + (i / Math.max(1, p.length - 1)) * (w - 2 * pad)
    const yy = h - pad - y * (h - 2 * pad)
    return `${x},${yy}`
  }).join(' ')
  const stroke = up ? '#34d399' : '#f87171'
  return (
    <svg className={className} viewBox={`0 0 ${w} ${h}`} preserveAspectRatio="none" aria-hidden>
      <polyline fill="none" stroke={stroke} strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" points={pts} />
    </svg>
  )
}
